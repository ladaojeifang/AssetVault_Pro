import { writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'
import { describe, expect, it } from 'vitest'
import { formatRootCssBlock } from '@/shared/themeRegistry'

const root = join(dirname(fileURLToPath(import.meta.url)), '../..')
const outPath = join(root, 'src/renderer/src/styles/theme-fallback.css')

describe('theme fallback css', () => {
  it('writes theme-fallback.css', () => {
    const css = `/* Auto-generated by pnpm run theme:gen — do not edit */\n${formatRootCssBlock('dark')}`
    writeFileSync(outPath, css)
    expect(css).toContain('--av-bg-primary')
  })
})
