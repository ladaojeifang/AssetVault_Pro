const path = require('path');
const { fsp } = require('../util/fs');
const { rgbaToBmp } = require('../util/bmp');

const DEFAULTS = {
  width: 512,
  height: 512,
  maxBytes: 64 * 1024,
  maxLines: 22,
  maxCols: 34,
};

async function readTextHead(filePath, maxBytes = DEFAULTS.maxBytes) {
  const fh = await fsp.open(filePath, 'r');
  try {
    const stat = await fh.stat();
    const size = Math.min(maxBytes, stat.size);
    const buf = Buffer.alloc(size);
    await fh.read(buf, 0, size, 0);

    let text = buf.toString('utf8');
    text = text.replace(/\r/g, '');
    text = text.replace(/\t/g, '  ');
    text = text.replace(/\u0000/g, '');

    return text;
  } finally {
    await fh.close();
  }
}

function extOf(filePath) {
  return path.extname(filePath).toLowerCase();
}

function pickBadge(ext) {
  if (ext === '.json') return { label: 'JSON', color: [227, 181, 5, 255] };
  if (ext === '.md') return { label: 'MD', color: [8, 63, 161, 255] };
  return { label: 'TXT', color: [102, 102, 102, 255] };
}

function setPixel(buf, imgW, imgH, x, y, rgba) {
  if (x < 0 || y < 0 || x >= imgW || y >= imgH) return;
  const idx = (y * imgW + x) * 4;
  buf[idx] = rgba[0];
  buf[idx + 1] = rgba[1];
  buf[idx + 2] = rgba[2];
  buf[idx + 3] = rgba[3];
}

function fillRect(buf, imgW, imgH, x, y, w, h, rgba) {
  for (let yy = y; yy < y + h; yy++) {
    for (let xx = x; xx < x + w; xx++) {
      setPixel(buf, imgW, imgH, xx, yy, rgba);
    }
  }
}

function drawChar5x7(buf, imgW, imgH, x, y, ch, rgba) {
  const glyph = FONT_5X7[ch] || FONT_5X7['?'];
  for (let row = 0; row < glyph.length; row++) {
    const bits = glyph[row];
    for (let col = 0; col < 5; col++) {
      if ((bits >> (4 - col)) & 1) {
        setPixel(buf, imgW, imgH, x + col, y + row, rgba);
      }
    }
  }
}

function drawText5x7(buf, imgW, imgH, x, y, text, rgba) {
  let cursorX = x;
  for (const ch of text) {
    drawChar5x7(buf, imgW, imgH, cursorX, y, ch, rgba);
    cursorX += 6;
  }
}

function rgbaToBGRA(rgba) {
  const out = Buffer.alloc(rgba.length);
  for (let i = 0; i < rgba.length; i += 4) {
    out[i] = rgba[i + 2];
    out[i + 1] = rgba[i + 1];
    out[i + 2] = rgba[i];
    out[i + 3] = rgba[i + 3];
  }
  return out;
}

async function extractTextLike(filePath, outDir, opts = {}) {
  const width = opts.width || DEFAULTS.width;
  const height = opts.height || DEFAULTS.height;
  const maxLines = opts.maxLines || DEFAULTS.maxLines;
  const maxCols = opts.maxCols || DEFAULTS.maxCols;
  const ext = extOf(filePath);
  const badge = pickBadge(ext);
  const outPath = path.join(outDir, path.basename(filePath) + '.bmp');

  const bg = [245, 245, 245, 255];
  const paper = [255, 255, 255, 255];
  const shadow = [210, 210, 210, 255];
  const ink = [51, 51, 51, 255];
  const subInk = [140, 140, 140, 255];

  const rgba = Buffer.alloc(width * height * 4);
  fillRect(rgba, width, height, 0, 0, width, height, bg);

  const margin = 36;
  const paperX = margin;
  const paperY = margin;
  const paperW = width - margin * 2;
  const paperH = height - margin * 2;

  fillRect(rgba, width, height, paperX + 8, paperY + 8, paperW, paperH, shadow);
  fillRect(rgba, width, height, paperX, paperY, paperW, paperH, paper);

  fillRect(rgba, width, height, paperX + 12, paperY + 12, 64, 24, badge.color);
  drawText5x7(rgba, width, height, paperX + 18, paperY + 20, badge.label, [255, 255, 255, 255]);

  const text = await readTextHead(filePath, opts.maxBytes || DEFAULTS.maxBytes);
  const lines = text
    .split('\n')
    .slice(0, maxLines)
    .map(line => line.slice(0, maxCols));

  let y = paperY + 56;
  for (const line of lines) {
    if (y + 7 > paperY + paperH - 20) break;
    drawText5x7(rgba, width, height, paperX + 18, y, line, ink);
    y += 16;
  }

  drawText5x7(
    rgba,
    width,
    height,
    paperX + 18,
    paperY + paperH - 20,
    path.basename(filePath).slice(0, 32),
    subInk
  );

  const bgra = rgbaToBGRA(rgba);
  const bmp = rgbaToBmp(width, height, bgra);
  await fsp.writeFile(outPath, bmp);

  return {
    ok: true,
    format: ext.slice(1) || 'text',
    type: 'bmp',
    outPath,
    width,
    height,
  };
}

